#include <cmath>
#include "Primitives.hpp"

/**
 * @param t_x Number.
 * @return The square root of @b t_x.
 */
f32 FSGSqrt(const f32 &t_x);

/**
 * @param t_x Number.
 * @return The next integer that's equal or greater than @b t_x.
 */
f32 FSGCeil(const f32 &t_x);

/**
 * @param t_x Number.
 * @return The next integer that's equal or less than @b t_x.
 */
f32 FSGFloor(const f32 &t_x);