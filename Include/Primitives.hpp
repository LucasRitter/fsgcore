#pragma once

#include <cinttypes>

#pragma region Signed Integers
typedef int8_t        i8;
typedef int16_t       i16;
typedef int32_t       i32;
typedef int64_t       i64;
#pragma endregion

#pragma region Unsigned Integers
typedef uint8_t         u8;
typedef uint16_t        u16;
typedef uint32_t        u32;
typedef uint64_t        u64;
#pragma endregion

#pragma region Floating Point Numbers
typedef float                 f32;
typedef double                f64;
#pragma endregion

#pragma region      Text
typedef char*       string;
typedef char const* static_string;
typedef char        character;
#pragma endregion